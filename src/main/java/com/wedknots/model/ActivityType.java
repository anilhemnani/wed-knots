package com.wedknots.model;

/**
 * Type of activity or ceremony in a wedding event
 */
public enum ActivityType {
    CEREMONY,       // Main ceremonies (e.g., Wedding, Haldi, Mehendi, Sangeet)
    ACTIVITY,       // General activities (e.g., Games, Performances)
    MEAL,           // Meal times (e.g., Lunch, Dinner, Breakfast)
    TRANSPORTATION, // Transportation related (e.g., Bus departure, Airport pickup)
    ACCOMMODATION,  // Accommodation related (e.g., Check-in, Check-out)
    OTHER           // Any other type
}

